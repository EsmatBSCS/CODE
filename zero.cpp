   //a program that inputs from user and determines whether it is positive, negative or zero.
 #include<iostream>
using namespace std;
int main()
{
	int n;
	cout<<"Enter the number ";
	cin>>n;
	if(n>0)
	cout<<"n is positive "<<endl;
	if(n<0)
	cout<<"n is negative "<<endl;
	if(n=0)
	cout<<"n is zero"<<endl;
	return 0;
	
} 
